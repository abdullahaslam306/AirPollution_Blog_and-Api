	<h3 style=" font-weight: bold;">How does air pollution spread and how can we handle this?</h3> </div>
	<div class="col-12">
	<p style="font-size: 16.5px;">

		The dispersion of air pollutants mainly depends on physical processes is air; those of wind and weather. How far air pollutants are transported mainly depends upon particle size of the compounds and at which height the pollution was emitted into the air. Fumes that are emitted into air through high smoke stags will mix with air so that local concentrations are not very high. However, wind will transport compounds and the pollution will become very disperse. Rain can remove pollutants from air. This causes precipitation and consequentially soil and water pollution.
		For environmental agencies it is very important to determine exactly how an air pollutant spreads. Air is not a very complex medium. This enables us to predict the dispersion of air pollutants with computer models. In a computer model dispersion is calculated by means of different parameters, such as wind speed, wind direction, temperature, air humidity and cloudiness. These predictions are of great significance when we are dealing with toxic clouds or radioactive radiation, because these are a danger to human health and because inhabitants of polluted areas need to be warned.




